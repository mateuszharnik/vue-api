<template>
  <section class="md:max-w-sm md:mx-auto rounded h-full bg-black overflow-hidden shadow-lg">
    <div class="px-6 py-6 flex flex-wrap">
      <div class="w-full mb-6 text-center font-bold text-xl capitalize">
        {{ result.name || result.title }}
      </div>
      <div class="w-full text-center">
        <router-link
          class="bg-yellow-dark no-underline hover:bg-yellow text-black
            font-bold py-2 px-4 rounded"
          :to="`${path}/${id}`"
          title="See more"
        >
          See more...
        </router-link>
      </div>
    </div>
  </section>
</template>

<script>
export default {
  name: 'card',
  props: {
    result: {
      type: Object,
      required: true,
    },
  },
  computed: {
    id() {
      const { url } = this.result;
      const index = url.substr(url.search(/[0-9]/));
      const id = parseInt(index, 10);

      return id;
    },
    path() {
      const { path } = this.$route;
      return path;
    },
  },
};
</script>
